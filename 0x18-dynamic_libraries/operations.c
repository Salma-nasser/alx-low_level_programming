#include <stdio.h>
/**
 * add - adds two int
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtracts two int
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two int
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two int
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - returns the remainder of two int
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
